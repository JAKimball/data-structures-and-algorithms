'use strict';

const { Stack, Queue } = require('../stacks-and-queues');

describe('Testing Stacks and Queues', () => {

  xtest('Can successfully push onto a stack', () => {
    const stack = new Stack();
  });

  xtest('Can successfully push multiple values onto a stack', () => {
    const stack = new Stack();
  });

  xtest('Can successfully pop off the stack', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully empty a stack after multiple pops', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully peek the next item on the stack', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully instantiate an empty stack', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully enqueue into a queue', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully enqueue multiple values into a queue', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully dequeue out of a queue the expected value', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully peek into a queue, seeing the expected value', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully empty a queue after multiple de-queues', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

  xtest('Can successfully instantiate an empty queue', () => {
    const stack = new Stack();
    const queue = new Queue();
  });

});
